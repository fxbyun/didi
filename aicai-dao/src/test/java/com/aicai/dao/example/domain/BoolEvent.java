package com.aicai.dao.example.domain;

public class BoolEvent {
	private long id;
	private boolean event;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public boolean isEvent() {
		return event;
	}

	public void setEvent(boolean event) {
		this.event = event;
	}

}
